'''
Programacion Orientada a Objetos:

La programación orientada a objetos (POO) en Python es un 
paradigma de programación que se basa en la creación y manipulación
de objetos. 

En POO, los objetos son instancias de clases, que son 
plantillas o moldes para crear objetos con características y 
comportamientos específicos. 

'''

'''
1- Clases: 
Las clases son la base de la POO en Python. Representan 
un conjunto de atributos (variables) y métodos (funciones) que 
describen el comportamiento de un objeto. Se definen utilizando la 
palabra clave class. 
'''

'''
2- Objetos: 
Los objetos son instancias de una clase. Cada objeto tiene 
sus propios atributos y métodos, pero comparten la misma estructura y 
comportamiento definidos por la clase.

3- Atributos: 
Los atributos son variables asociadas a un objeto. Pueden 
ser variables de instancia (propiedades únicas para cada objeto) o 
variables de clase (propiedades compartidas por todos los objetos de la 
misma clase).

4- Métodos: 
Los métodos son funciones definidas dentro de una clase y 
están asociados a los objetos de esa clase. Pueden acceder y modificar 
los atributos del objeto.

'''
'''
5-Encapsulación: 
En Python, la encapsulación se logra mediante el uso 
de convenciones de nomenclatura. Los atributos y métodos que comienzan 
con un solo guion bajo (_) se consideran privados y no deberían 
accederse desde fuera de la clase. Aquellos que comienzan con dos 
guiones bajos (__) tienen un nivel de encapsulación más fuerte y no se 
pueden acceder directamente desde fuera de la clase.

6- Herencia: 
La herencia permite crear una nueva clase basada en una clase
existente. La nueva clase (llamada clase derivada o subclase) hereda los 
atributos y métodos de la clase base (llamada clase padre o superclase) y 
puede agregar sus propios atributos y métodos adicionales.

7- Polimorfismo: 
El polimorfismo se basa en la herencia y la capacidad de 
una clase derivada (subclase) de heredar y modificar el comportamiento 
de una clase base (superclase). Esto significa que una clase derivada 
puede implementar su propia versión de un METODO heredado de la clase base.

8- Composicion: 
La composición es un concepto en programación orientada a objetos que 
describe una relación entre objetos donde un objeto contiene a otro objeto 
como parte de su estructura interna. En la composición, un objeto se 
compone de uno o más objetos más pequeños, y estos objetos más pequeños 
existen como componentes dentro del objeto mayor.
'''
